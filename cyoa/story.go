// Functional Options to implement custom paths
type HandlerOption func(h *handler)

func WithTemplate(t *template.Template) HandlerOption {
	return func(h *handler) {
		h.t = t
	}
}

func WithPathFunctions(fn func(r *http.Request) string) HandlerOption {
  return func(h *handler) {
    h.pathFn = fn
  }
}

// Following the GO idiom - return structs, accept interfaces

// Let the user pass the template
// Can be no template or custom template or default template
func NewHandler(s Story, opts ...HandlerOption) http.Handler {
	h := handler{s, tpl, defaultPathFn}
	for _, opt := range opts {
		opt(&h)
	}
	return h
}

type handler struct {
	s      Story
	t      *template.Template
	pathFn func(r *http.Request) string
}

// Functional Options
func defaultPathFn(r *http.Request) string {
	// Which chapter we are on
	// Which path to parse
	path := strings.TrimSpace(r.URL.Path)
	// If no path or no story available then go back to intro page
	if path == " " || path == "/" {
		path = "/intro"
	}
	// Remove the initial slash, "/intro" -> "intro"
	return path[1:]
}

// Users need to generate the template
func (h handler) ServeHTTP(w http.ResponseWriter, r *http.Request) {
  path := h.pathFn(r)
	// Proceed only if you found something in the map
	if chapter, ok := h.s[path]; ok {
		err := h.t.Execute(w, chapter)
		if err != nil {
			log.Printf("%v", err)
			http.Error(w, "Something went wrong...", http.StatusInternalServerError)
		}
		return
	}
	http.Error(w, "Chapter not found.", http.StatusNotFound)

}

func JsonStory(r io.Reader) (Story, error) {
	d := json.NewDecoder(r)
	var story Story
	if err := d.Decode(&story); err != nil {
		return nil, err
	}

	return story, nil
}

type Story map[string]Chapter

type Chapter struct {
	Title      string   `json:"title"`
	Paragraphs []string `json:"story"`
	Options    []Option `json:"options"`
}

type Option struct {
	Text    string `json:"text"`
	Chapter string `json:"arc"`
}

